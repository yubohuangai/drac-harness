#!/bin/bash
# setup.sh — first-time configuration for drac-harness
#
# Prompts the user for their cluster username, RAC account, and default
# account, then writes the values to ~/.config/drac-harness/user.conf.
#
# After this runs, `bin/install-claude-md.sh` reads user.conf and
# substitutes placeholders into the per-cluster canonical when installing
# to ~/.claude/CLAUDE.md.
#
# Re-run any time to update values — the script overwrites user.conf
# (after backing up to user.conf.bak.<timestamp>).

set -euo pipefail

CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/drac-harness"
CONFIG_FILE="$CONFIG_DIR/user.conf"

mkdir -p "$CONFIG_DIR"

echo "drac-harness — first-time setup"
echo "==============================="
echo
echo "This collects three values used to substitute placeholders in the"
echo "per-cluster CLAUDE.md template at install time:"
echo "  USER_NAME    — your username on this cluster"
echo "  RAC_ACCOUNT  — RAC priority allocation (typically rrg-<pi-lastname>)"
echo "  DEF_ACCOUNT  — default allocation (typically def-<pi-lastname>)"
echo
echo "Values are stored in $CONFIG_FILE — never committed to git."
echo

# USER_NAME — default to $USER
read -rp "Cluster username [$USER]: " USER_NAME
USER_NAME="${USER_NAME:-$USER}"

# Try to detect accounts via sshare -U if available.
# Some clusters (e.g. Rorqual) split base accounts into per-partition
# rows (def-vislearn_cpu, rrg-vislearn_gpu, …); strip those suffixes
# and dedupe to recover the base account names that --account= expects.
DETECTED_ACCOUNTS=""
if command -v sshare >/dev/null 2>&1; then
    DETECTED_ACCOUNTS=$(sshare -U --noheader --format=Account 2>/dev/null | \
                       grep -E '^(rrg|def|ctb)-' | \
                       sed -E 's/_[a-z]+[[:space:]]*$//; s/[[:space:]]+$//' | \
                       sort -u | tr '\n' ' ' || true)
    if [ -n "$DETECTED_ACCOUNTS" ]; then
        echo
        echo "Detected accounts via 'sshare -U':$DETECTED_ACCOUNTS"
    fi
fi

# Best guess for RAC default
RAC_DEFAULT=""
DEF_DEFAULT=""
if [ -n "$DETECTED_ACCOUNTS" ]; then
    RAC_DEFAULT=$(echo "$DETECTED_ACCOUNTS" | tr ' ' '\n' | grep '^rrg-' | head -1 || true)
    DEF_DEFAULT=$(echo "$DETECTED_ACCOUNTS" | tr ' ' '\n' | grep '^def-' | head -1 || true)
fi

echo
read -rp "RAC account (rrg-…)${RAC_DEFAULT:+ [$RAC_DEFAULT]}: " RAC_ACCOUNT
RAC_ACCOUNT="${RAC_ACCOUNT:-$RAC_DEFAULT}"
if [ -z "$RAC_ACCOUNT" ]; then
    echo "ERROR: RAC_ACCOUNT is required (e.g. rrg-smith). Run 'sshare -U' to see your accounts." >&2
    exit 1
fi

read -rp "Default account (def-…)${DEF_DEFAULT:+ [$DEF_DEFAULT]}: " DEF_ACCOUNT
DEF_ACCOUNT="${DEF_ACCOUNT:-$DEF_DEFAULT}"
if [ -z "$DEF_ACCOUNT" ]; then
    echo "ERROR: DEF_ACCOUNT is required (e.g. def-smith). Run 'sshare -U' to see your accounts." >&2
    exit 1
fi

# Backup existing config
if [ -f "$CONFIG_FILE" ]; then
    BACKUP="$CONFIG_FILE.bak.$(date +%Y%m%d_%H%M%S)"
    cp "$CONFIG_FILE" "$BACKUP"
    echo
    echo "[setup] backed up existing config → $BACKUP"
fi

cat > "$CONFIG_FILE" <<EOF
# drac-harness user config
# Generated by bin/setup.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ)
# Substituted into wiki/canonicals/*.md by bin/install-claude-md.sh.
# Re-run setup.sh to update.

USER_NAME="$USER_NAME"
RAC_ACCOUNT="$RAC_ACCOUNT"
DEF_ACCOUNT="$DEF_ACCOUNT"
EOF

chmod 600 "$CONFIG_FILE"

echo
echo "[setup] wrote $CONFIG_FILE:"
echo "  USER_NAME=$USER_NAME"
echo "  RAC_ACCOUNT=$RAC_ACCOUNT"
echo "  DEF_ACCOUNT=$DEF_ACCOUNT"
echo
echo "Next: run bin/install-claude-md.sh to install the per-cluster"
echo "      CLAUDE.md to ~/.claude/CLAUDE.md."
